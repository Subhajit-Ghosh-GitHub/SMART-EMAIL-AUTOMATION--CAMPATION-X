    def select_best_variant(self, perf_a, perf_b):

        score_a = perf_a["open_rate"] + perf_a["click_rate"]
        score_b = perf_b["open_rate"] + perf_b["click_rate"]

        if score_a > score_b:
            print("Variant A performed better")
            return perf_a
        else:
            print("Variant B performed better")
            return perf_b

    def find_micro_segments(self, report_data):

        df = pd.DataFrame(report_data)
        opened = df[df["EO"] == "Y"]

        print("Micro segment size:", len(opened))
        return opened

    def micro_segment(self, report):

        df = pd.DataFrame(report["data"])

        if "EO" not in df.columns:
            return self.segmented_customers

        opened = df[df["EO"] == "Y"]

        if "customer_id" not in df.columns:
            return self.segmented_customers

        opened_ids = set(opened["customer_id"])

        micro_segment = [
            c for c in self.segmented_customers
            if c["customer_id"] in opened_ids
        ]

        print(f"Micro-segment identified: {len(micro_segment)} high-interest users")

        if len(micro_segment) > 0:
            return micro_segment

        return self.segmented_customers

