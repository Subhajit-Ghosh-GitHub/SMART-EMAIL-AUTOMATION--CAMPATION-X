    def segment_customers(self, parsed_brief):
        target = parsed_brief.get("target_segments", [])
        segment = []

        for c in self.customers:
            age = c.get("age", 0)
            gender = c.get("gender", "").lower()

            if "female senior citizens" in str(target).lower():
                if gender == "female" and age >= 60:
                    segment.append(c)

        if not segment:
            segment = self.customers

        print(f"Segment selected: {len(segment)} customers")
        return segment

